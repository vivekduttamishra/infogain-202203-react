

//we can create object simply by calling new Object()
var p = new Object();



//p is a new object. We don't know yet, what does it represent
console.log(typeof p,p);  //we have an empty object yet.


//how do I know what are the properties of this object?
//we can assign the properties, after creating the Object


p.name="Sanjay";  //p has a name
p.age=40;         //p has an age.

console.log(typeof p,p);

console.log('p.name',p.name);

console.log('p.age',p.age);













